with open("/home/chang/map_merge/new/map1_c.txt","r") as f:
  lines = f.readlines()
  for i, line in enumerate(lines):
    cc = line.split()
    x = float(cc[0])
    y = float(cc[1])
    z = float(cc[2])
    r = int(cc[3])
    g = int(cc[4])
    b = int(cc[5])
    if r == 255 and g == 255 and b == 255 :
      t.write(line)

